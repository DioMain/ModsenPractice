function solution(numbers = []) {
  const gcd = (a, b) => b === 0 ? a : gcd(b, a % b);

  let result = numbers[0];
  for (let i = 1; i < numbers.length; i++) {
    result = gcd(result, numbers[i]);
  }

  return result * numbers.length;
}

